# Lógica E = & : Pra ser verdade todos tem que ser verdadeiros

6 == 6 
7 == 7 & 1 == 1
10 == 3 & 3 == 10
15 == 15 & 3 == 3 
1 <= 1 & 3 > 5
10 != 5  & 10 > 20


# Lógica ou = | : pra ser verdade basta um caso ser verdadeiro

20 > 10 | 3 > 1 
3 > 2 | 2 > 1 
10 < 1 | 2 < 0
20 != 20 | 2 > 0


# Negação, siginifica o inverso do resultado

!10 == 10
!15 < 20
!5 < 0



